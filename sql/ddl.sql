-- 무서운이야기 테이블
create table HORROR_STORY
(
    HORR_ST_NO bigint generated by default as identity NOT NULL,
    VIEW_COUNT int,
    TITLE varchar(255),
    CONTENT varchar(4000),
    ENTER_BY varchar(40),
    ENTER_DATE date,
    UPDATE_BY varchar(40),
    UPDATE_DATE date,
    PRIMARY KEY (HORR_ST_NO)
)